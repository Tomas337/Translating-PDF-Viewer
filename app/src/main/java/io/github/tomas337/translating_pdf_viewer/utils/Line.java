package io.github.tomas337.translating_pdf_viewer.utils;

public class Line {

    float x;
    float y;
    float width;
    float height;

    public Line(
            float x,
            float y,
            float width,
            float height
    ) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
